import math
def addition(a,b):
    a = int(a)
    b = int(b)
    c = a + b
    return c
def sub(a, b):
    a = float(a)
    b = float(b)
    c = b - a
    return c
def times(a, b):
    a = float(a)
    b = float(b)
    c = a * b
    return c
def div(a, b):
    a = float(a)
    b = float(b)
    c = b / a
    return c
def square(a):
    a = float(a)
    c = a * a
    return c
def sqrt(a):
    a = float(a)
    c = math.sqrt(a)
    return c